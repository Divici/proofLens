function fmtCellValue(value: string | number | boolean | null): string {
  if (value === null) return "—";
  if (typeof value === "boolean") return value ? "yes" : "no";
  return String(value);
}

interface ReviewReportProps {
  review: Review;
  /**
   * `data:` URL of the 256-px JPEG thumbnail. The caller decodes the Blob
   * once and passes a string so this component stays pure / sync.
   */
  thumbnailDataUrl: string;
  /** App version, e.g. from `package.json#version`. */
  appVersion: string;
}

export function ReviewReport({
  review,
  thumbnailDataUrl,
  appVersion,
}: ReviewReportProps) {
  const overrideRows = review.fieldResults.filter(
    (fr): fr is FieldResult & { humanOverride: NonNullable<FieldResult["humanOverride"]> } =>
      Boolean(fr.humanOverride),
  );
  const decisionText = review.decision?.decision ?? "no decision";
  const reviewerNotes = review.decision?.notes ?? "";

  return (
    <Document
      title={`proofLens review — ${review.brand}`}
      author={review.reviewerName}
      creator="proofLens"
      producer="proofLens"
    >
      <Page size="LETTER" style={styles.page} wrap>
        {/* 1. Header */}
        <View style={styles.headerRow}>
          <View>
            <Text style={styles.brand}>proofLens</Text>
            <Text style={styles.brandSub}>
              Alcohol-label compliance review
            </Text>
          </View>
          <Text style={styles.versionTag}>v{appVersion}</Text>
        </View>

        {/* 2. Metadata */}
        <View style={styles.metadataGrid}>
          <View style={styles.metadataCell}>
            <Text style={styles.metadataLabel}>Brand</Text>
            <Text style={styles.metadataValue}>{review.brand}</Text>
          </View>
          <View style={styles.metadataCell}>
            <Text style={styles.metadataLabel}>Beverage type</Text>
            <Text style={styles.metadataValue}>{review.beverageType}</Text>
          </View>
          <View style={styles.metadataCell}>
            <Text style={styles.metadataLabel}>Reviewer</Text>
            <Text style={styles.metadataValue}>{review.reviewerName}</Text>
          </View>
          <View style={styles.metadataCell}>
            <Text style={styles.metadataLabel}>Completed at</Text>
            <Text style={styles.metadataValue}>
              {formatCompletedAt(review.createdAt)}
            </Text>
          </View>
          <View style={styles.metadataCell}>
            <Text style={styles.metadataLabel}>Rules version</Text>
            <Text style={styles.metadataValue}>{review.rulesVersion}</Text>
          </View>
          <View style={styles.metadataCell}>
            <Text style={styles.metadataLabel}>Review id</Text>
            <Text style={styles.metadataValue}>{review.id}</Text>
          </View>
        </View>

        {/* 3. Thumbnail.
            `@react-pdf/renderer` <Image> doesn't support an alt prop —
            jsx-a11y is a DOM-side rule and we suppress it here. */}
        {thumbnailDataUrl ? (
          // eslint-disable-next-line jsx-a11y/alt-text
          <Image src={thumbnailDataUrl} style={styles.thumbnail} />
        ) : null}

        {/* 4. Field-by-field table */}
        <Text style={styles.sectionTitle}>Verification details</Text>
        <View style={styles.table}>
          <View style={styles.tableHeaderRow}>
            <Text style={[styles.cell, styles.cellField]}>Field</Text>
            <Text style={[styles.cell, styles.cellExpected]}>Expected</Text>
            <Text style={[styles.cell, styles.cellExtracted]}>Extracted</Text>
            <Text style={[styles.cell, styles.cellStatus]}>Status</Text>
            <Text style={[styles.cell, styles.cellConfidence]}>Conf.</Text>
          </View>
          {review.fieldResults.map((fr) => (
            <View style={styles.tableRow} key={fr.field} wrap={false}>
              <Text style={[styles.cell, styles.cellField]}>{fr.label}</Text>
              <Text style={[styles.cell, styles.cellExpected]}>
                {fmtCellValue(fr.expected)}
              </Text>
              <Text style={[styles.cell, styles.cellExtracted]}>
                {fmtCellValue(fr.value)}
              </Text>
              <Text style={[styles.cell, styles.cellStatus]}>
                {STATUS_LABEL[fr.status]}
              </Text>
              <Text style={[styles.cell, styles.cellConfidence]}>
                {fr.confidence.toFixed(2)}
              </Text>
            </View>
          ))}
        </View>

        {/* 5. Overall verdict — coloured pill keyed off OverallStatus. */}
        <Text style={styles.sectionTitle}>Overall verdict</Text>
        <View
          style={[
            styles.verdictPill,
            { backgroundColor: VERDICT_COLOURS[review.overall] },
          ]}
        >
          <Text style={styles.verdictLabel}>Verdict</Text>
          <Text style={styles.verdictValue}>
            {VERDICT_LABEL[review.overall]}
          </Text>
        </View>
        <Text style={styles.metadataValue}>
          Final decision: {decisionText}
        </Text>

        {/* 6. Human overrides — only when present */}
        {overrideRows.length > 0 ? (
          <>
            <Text style={styles.sectionTitle}>Human overrides</Text>
            {overrideRows.map((fr) => (
              <View style={styles.overrideRow} key={`ov-${fr.field}`} wrap={false}>
                <Text style={styles.metadataValue}>
                  {fr.label}: AI: {fr.humanOverride.originalAiStatus} →
                  {" "}Human: {fr.humanOverride.humanStatus}
                </Text>
                <Text style={styles.metadataLabel}>
                  Reason
                </Text>
                <Text style={styles.metadataValue}>
                  {fr.humanOverride.reason || "(no reason given)"}
                </Text>
                <Text style={styles.brandSub}>
                  by {fr.humanOverride.reviewerName} at {fr.humanOverride.timestamp}
                </Text>
              </View>
            ))}
          </>
        ) : null}

        {/* 7. Reviewer notes */}
        {reviewerNotes ? (
          <>
            <Text style={styles.sectionTitle}>Reviewer notes</Text>
            <View style={styles.notesBlock}>
              <Text>{reviewerNotes}</Text>
            </View>
          </>
        ) : null}

        {/* Signature line */}
        <View style={styles.signature}>
          <Text>{SIGNATURE_LINE}</Text>
          <Text style={styles.brandSub}>
            Reviewer: {review.reviewerName}
          </Text>
        </View>

        {/* Footer (fixed) */}
        <View style={styles.footer} fixed>
          <Text>{buildRulesFooter(review.rulesVersion)}</Text>
          <Text>{THUMBNAIL_FOOTER_NOTE}</Text>
        </View>
      </Page>
    </Document>
  );
}

ReviewReport.displayName = "ReviewReport";
