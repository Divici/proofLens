/**
 * `<ReviewReport>` — `@react-pdf/renderer` React component that renders
 * the per-label PDF audit artifact.
 *
 * Layout (top → bottom):
 *   1. Header  — proofLens wordmark + app version
 *   2. Metadata — reviewer name, completed-at timestamp, beverage type,
 *                 brand, rules version
 *   3. Image   — the 256-px thumbnail (data: URL passed in by the caller —
 *                 keeps this component pure / sync / server-safe)
 *   4. Field-by-field table — expected vs extracted, status, confidence
 *   5. Overall verdict — pass/fail/etc. styled pill
 *   6. Human override section — only if at least one field carries
 *      `humanOverride`
 *   7. Reviewer notes — `decision.notes` (audit field)
 *   8. § 16.21 footer — canonical compliance statement
 *   9. Signature line — "Final decision is the reviewer's responsibility."
 *
 * The thumbnail-not-original disclaimer is in the footer per the data-
 * handling policy: originals never leave the request that produced them.
 *
 * Fonts: we deliberately avoid loading custom fonts here. `@react-pdf/
 * renderer` ships Helvetica / Times-Roman / Courier as built-ins; using
 * those keeps the bundle small and removes a source of Vercel cold-start
 * failures. If we want Inter later, register it from `public/fonts/`.
 */

